package com.excript;

public class EX_A29_For {

    public static void main(String[] args) {

        for (byte i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println("\nO for possui 3 partes: \n");
        System.out.println("1. Variavel;");
        System.out.println("2. Condicao;");
        System.out.println("3. Incremento.");
    }

}
