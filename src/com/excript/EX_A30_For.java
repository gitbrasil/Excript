package com.excript;

public class EX_A30_For {

    public static void main(String[] args) {
		
	for (
		int i = 0;	// iniciar
		i <= 5 ;    	// condicionar
		i++		// atualizar
            ) 
            {
		System.out.println(i);
            }

	}

}
