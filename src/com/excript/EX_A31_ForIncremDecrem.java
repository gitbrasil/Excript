package com.excript;

public class EX_A31_ForIncremDecrem {

    public static void main(String[] args) {

        for (int a = 0, b = 10; a <= 10; a++, b--) {
            System.out.print(a + "\t-\t" + b);
            System.out.println();
        }

    }

}
