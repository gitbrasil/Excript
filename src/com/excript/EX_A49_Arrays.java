package com.excript;

public class EX_A49_Arrays {

    public static void main(String[] args) {

        System.out.println("Revisao dos Arrays");
        System.out.println("Declaracao do Array: \n");
        System.out.println("String[] meuArray = new String[5]");
        //
        String[] meuArray = new String[5];
        //
        System.out.println("");
        System.out.println("'String[]' \t- Array do tipo String");
        System.out.println("'meuArray' \t- atribuir nome para o Array (variavel referencia)");
        System.out.println("'new' \t\t- atribuir nova estancia");
        System.out.println("'String' \t- Array de String");
        System.out.println("'[5]' \t\t- Qtde de elementos do tipo String");
        System.out.println("");
        System.out.println("O Java aloca espaco na memoria para 5 variaveis do tipo String. ");
        System.out.println("e atribui a referencia das variaveis ao objeto meuArray,");
        System.out.println("ou seja ha um objeto do tipo Array + 5 do tipo String");
        System.out.println("");
        System.out.println("Os objetos do tipo String estao vinculados ao objetos Array");
        System.out.println("");
                
                
    }

}
